
package practica_1;

public class Calculadora {
   
    public int Suma(int x, int y){
        return(x+y);
    }
    public int Resta(int x, int y){
        return (x-y);
    }
    public int Dividir(int x, int y){
        return (x/y);
    }
    public int Multiplicar(int x, int y){
        return (x*y);
    }
}
